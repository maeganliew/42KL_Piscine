#include "ft.h"

int	addition(int i, int k)
{
	return (i + k);
}

int	subtraction(int i, int k)
{
	return (i - k);
}

int	multiplication(int i, int k)
{
	return (i * k);
}

int	modulo(int i, int k)
{
	return (i % k);
}

int	division(int i, int k)
{
	return (i / k);
}
